package Task2;

public class Methods {
    double a;
    double b;

    public double Plus(double a, double b) {
        return  a + b;
    }

    public double Minus(double a, double b) {
        return a - b;
    }

    public double Div(double a, double b) {
        return a / b;
    }

    public double Multi(double a, double b) {
        return a * b;
    }
}
